                   <b> Физическо Почистване на Кристал</b>
                     <br>
                    <br>
                    Ако сте закупили кристал, върху който все още има пръст и скални отломъци, не се притеснявайте, тъй като те се част от матрицата на растежа на самия кристал. Матрицата е частта от земята, върху която израства кристалът и е пълна с минерални вещества, които го „подхранват“. Опитайте се първо, като използвате суха четка, да отстраните това, което можете, но бъдете внимателни, тъй като кристалът може да бъде по-мек от самата матрицата, в която е израстнал. След това използвайте топла водна баня и по-твърда почистващата четка за по-едрите кристали или гъвкава, средно-мека четка за зъби за по-крехките такива. Понякога може да използвате и силно пенлив препарат за миене на съдове и по-твърда четка за зъби, като с помощта на пяната да се почистят песъчинките и частиците от ъгълчетата. Матрицата на кварцовите кристали обикновено е жълта към каки и по-упоритите частици кал могат да бъдат отстранени чрез баня с оксалова киселина от 2 до 5 дни – разбира се, ако обикновеното измиване с вода не е подействало.
                  
                    Използвайте 1 част оксалова киселина и 2 части вода. Що се отнася до „нормалната“ кал, по-скоро използвайте топла вода, вана и / или струя под високо налягане, за да отмиете калта. Можете да почистите калта и с твърда дървена клечка.Не използвайте метални инструменти, тъй като най-вероятно те ще надраскат или счупят кристала, понеже стоманата е по-твърда, отколкото кристала. После оставете кристала да изсъхне на слънце и да „прихване“ от неговите лъчи!
