                    <b> Енергиино Почистване на Кристал</b>
                    <br>
                    <br>
                    След като физически сте почистили кристала, следващата стъпка е да го почистите от нежеланите вибрации и енергии, дошли от хората, които са работили с кристала по време на неговото добиване, опаковане, транспортиране, разопаковане и показване, преди изобщо да е пристигнал при вас. Кристалите са склонни да поемат безразборно енергията и също като при носенето на чужда дреха, можете да почувствате, че енергията му не е точно вашата.
                    
                    Всички сме чели или чували, че най-подходящата техника за почистването на кристалите е като ги заровим в морска сол за период от няколко дни до няколко седмици. Просто усетете коя техника е за вас, коя ви звучи смислено и експериментирайте. Някои препоръчват използването на солна баня за кристали, които са в състояние да я понесат. Наистина солта е добро за премахване на негативните енергии от повечето кристали. Въпреки всичко, се съобразете с това, че някои кристали като Опалите, Лаписа, Пирита, Хематита и др. са чувствителни към солта и тяхната повърхност може много бързо да се повреди в солна баня.
                    
                    В случай че сте решили да използвате тази техника, имайте предвид, че кристалите не трябва да се държат накиснати много дълго в солената вода. Седем часа обикновено са достатъчни за тяхното почистване, освен ако кристалите са изключително замърсени поради някаква причина. Когато сте имали наистина тежко почистване на клиент (ниво екзорсизъм), тогава оставете кристалите в силно наситена солена вода в продължение на 3-4 дни или дори за по-дълго. При нормална ежедневна „употреба“ на кристали, като носенето им в джоб или на висулка и пр., лека 7-часова баня на всеки 2 седмици е достатъчна. За целта вземете пластмасова или стъклена купа достатъчно голяма, за да побере вашия кристал(и). Стъкленият съд е добър, но повърхността му е твърда и бихте могли по невнимание да счупите или пукнете вашите кристали. Напълнете купата с вода, така че тя да покрие кристалите с около 2-3 см. Добре е водата да е със стайна температура, а най-добре би било, ако използвате изворна вода, която е „жива“ и съдържа „хранителни вещества“, които кристали обичат. 
                    
                 
                    Може да използвате и дестилирана вода за почистване на по-финни енергии. За кристала точната температура на водата няма значение, освен ако не изпадате в крайности като използването на много гореща и много студена такава. След това, за всеки кристал, добавете 1 супена лъжица сол (ориентировъчно). „Перфектният“ разтвор за почистване е 30%-вият разтвор на морска сол в изворна вода, т.е. 30гр. морска сол в 100мл. изворна вода. Също, ако имате истинска морска вода, можете да я използвате! Обикновено, просто хвърлете шепа сол върху кристалите и оставете съда на слънце, за да се накиснат.
                    
                    Опитайте се да използвате махало, което ще ви помогне да определите кога кристалите са готови и могат да бъдат извадени от банята. Също така можете, минавайки покрай почистващите се кристали, да се опитате интуитивно да узнаете дали са „готови“. Когато изливате солената вода, внимавайте да не я докосвате, за да не прехвърлите негативните енергии върху себе си!  После не забравяйте обилно да изплакнете всеки кристал под течаща вода, за да отстраните остатъците от солената вода. Оставете кристалите върху хартия или кърпа за подсушаване.
                    
                
                    
                    Някои хора се питат дали има разлика между използването на морска сол и готварска сол (каменна сол). Да, има! Когато станете по-чувствителни към фините енергии сами ще я доловите. Морската сол оставя почистените кристали с по-различно усещане от готварската. Морска сол има по-фин енергиен „подпис“, с множество малки вибрации, оцветени с цветовете на дъгата. Трапезната сол е като впрегатен кон – не толкова изтънчен, по-тромав и притежава от по-тежките жълти и сиво-сини вибрации. Ако планирате да използвате кристали за по-горните чакри (3-то око или Коронна), почистете ги с морска сол, тъй като тези центрове са много фини и чувствителни към по-фини вибрации. Просто опитайте да накиснете два кристала – един в морска сол и друг в готварска сол. После ги изплакнете обилно и след това вижте дали ще можете да усетите енергийно разликата между тях.
                    
                    Друг популярен метод за почистване чрез сол е като напълните до половината стъклена или пластмасова купа с морска сол и поставете кристалите директно в солта. След това или досипете сол, за да ги затрупате или ги оставете така на повърхността. Отново трябва да ги оставите в солта поне за няколко часа, най добре е за една нощ или в продължение на няколко дни. После трябва да изплакнете кристали обилно под хладка течаща вода, за да премахнете всякаква останала по тях сол. След употребата на сол, тя се изхвърля и никога не се използват повторно! Иначе поставените кристали ще усвоят негативните и нежелани енергии от първоначалното почистване. При този метод също трябва да се съобразявате с видовете кристали, които поставяте директно върху сухата сол, за да няма неблагоприятен ефект върху тях, както е обяснено по-горе.
                    
                    Безконтактното почистване със сол се смята за най-сигурният метод за почистване със сол и се използва, когато искате да предпазите кристалите от увреждане при директен контакт със солта. 
                   
                    Добър метод, особено за почистване на скъпоценни камъни, поставени в бижута или някакъв метал. Първо, напълнете стъклена купа наловина или до две трети със суха морска сол. След това поставете по-малък стъклен съд или плитка чаша в купата и я заровете наполовина в солта. После можете да поставите вашите кристали директно в празната чаша, както си стои половината заровена в сухата сол. Солта ще бъде в състояние да изтегли запазените в камъните енергии, но този метод ще отнеме повече време, отколкото прекия контакт със солта. Можете също да налеете достатъчно вода (за предпочитане изворна, дестилирана или минерална вода) в малкия стъклен съд – достатъчно, за да покрие кристалите или бижутата. Това ще ги защити от прякото „полагане върху сол“ или „от соления въздух“, предизвикващи корозия или неблагоприятни последици за всички метали, обграждащи скъпоценните камъни. Също така, имайте предвид, че някои кристали не трябва да се оставят или да се накисват във вода, напр. селенит. Отново използваната сол трябва да бъде изхвърлена и никога да не се използва повторно!
                    
                   
                    Всички кристали могат да се почистват безопасно, когато ги поставите върху друза от Аметистза една нощ или за 24-48 часа. Кварцовата друза може да се използва по същия начин. Чистият и бистър кварц осъществява този процес на прочистване по-бързо, отколкото Аметиста, но и двата метода са еднакво ефективни. Просто поставетене повече от няколко кристала върху кристалните връхчета на друзата и ги оставете така за 24 до 48 часа. Тези друзи и кристални клъстерни формирования (както Аметиста и Кварца) имат способността да абсорбират енергиите, съдържащи се в поставените върху тях кристали, да ги неутрализират и след това да излъчат „добра“енергия от друзата обратно във вече почистените кристали, така че те могат ефективно и да ги почистват, и да ги енергизират в същото време.
                    
                    За почистването на кристалите също можете да използвате окъдяването им с бял градински чай, сладка трева, сандалово и кедрово дърво, борови иглички и кора или тамян. Тази техника е особено подходяща за кристали, които не могат да бъдат накисвани в солена вода. Използвайки ветрило или просто пера за период от 3 до 7 минути се навява дима от тамяна върху кристалите. Това е особено подходяща техника, ако имате цял шкаф пълен с кристали или някои от тях са много големи и неудобни, за да им се правят бани. Опитайте се да не използвате ръката си като ветрило – тъй като, когато димът се издига, той издърпва и отнася негативните енергии от кристала, а минавайки с ръката си през този дим можете да прихванете и „попиете“ тези негативни енергии върху себе си. Не забравяйте добре да окъдите и себе си, за да изчистите вашите собствени негативни енергии, особено от ръцете, които директно работят с кристалите. Можете също да изплакнете ръцете си в солена вода, след като сте работили с кристали, както със себе си, така и с някой друг. По общо мнение кристалите най-много обичат дима от белия градински чай….
                    
                    Бърз начин за почистване на кристали е като се държат под струя хладка вода за 3-5минути. Внимание: някои кристали не трябва да се третират с вода, важи особено за меките камъни като Азурит, Ангелит, Фенакит, Селенит и др. подобни.
                   
                    Друг бърз начин за почистване на всякакви кристали е чрез поставянето им върху парче Селенит. Ако сте прочели пълното описание на Селенита, вече ще знаете, че той изобщо няма нужда да бъде почистван и има свойството да абсорбира отрицателната енергия. Освен това, селенитът почиства останалите кристали и ги презарежда! Можете просто да поставите вашия кристал за около 10 минути върху парче Селенит и сте готови! Най-добре е ако вечер поставяте кристалите, които носите през деня със себе си, върху парче Селенит, а сутрин – обратно, камъните, които сте сложили под възглавницата предната вечер (напр. Аметиста, който и без това не обича слънчевата светлина) ги оставите през деня отново върху Селенита. Така едновременно се почистват и презареждат – едните през нощта, докато спите, а другите през деня, докато си вършите ежедневните работи. Селенитът е един от най-добрите минерали за почистване и презареждане на кристалите.
                    
